
# Es una función que imprime en pantalla el texto que le pasamos como argumento.
# Puede recibir varios argumentos, que se separan por comas.
# Por defecto, añade un espacio entre los argumentos que le pasamos.
# Podemos cambiar el separador entre los argumentos con el parámetro sep.
# También podemos cambiar el final de la línea con el parámetro end.

print("Hola, mundo")
print('Hola, mundo')
print("Python", "es", "genial")
print("Python", "es", "genial", sep="-")
print("Esto se imprime", end=" ")
print("en la misma línea")