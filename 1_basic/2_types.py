
# Tipos de datos en Python


print("int:")
print(type(10))
print(type(-10))

print("float:")
print(type(10.5))
print(type(-10.5))
print(type(10.0))

print("complex:")
print(type(10 + 5j)) # matematecas, cientifico, ingenieria

print("str:")
print(type("10"))
print(type('hola'))
print(type(""))
print(type('''
multilinea
           '''))

print("bool:")
print(type(True))
print(type(False))
print(type(10 > 5))

print("NoneType:")
print(type(None)) # no tiene valor, no existe, no se ha inicializado
